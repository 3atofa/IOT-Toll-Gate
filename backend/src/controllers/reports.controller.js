const PDFDocument = require('pdfkit');
const { Op } = require('sequelize');
const { GateCapture, SecurityAlert, Vehicle, AllowedCard } = require('../models');

const buildDateRange = (req) => {
  const start = req.query.startDate ? new Date(req.query.startDate) : null;
  const end = req.query.endDate ? new Date(req.query.endDate) : null;

  return {
    start: Number.isNaN(start?.getTime?.()) ? null : start,
    end: Number.isNaN(end?.getTime?.()) ? null : end,
  };
};

const fetchSummaryData = async (req) => {
  const { start, end } = buildDateRange(req);
  const captureWhere = {};
  if (start || end) {
    captureWhere.capturedAt = {};
    if (start) captureWhere.capturedAt[Op.gte] = start;
    if (end) captureWhere.capturedAt[Op.lte] = end;
  }

  const [totalCaptures, accessGranted, accessDenied, securityChecks, totalAlerts, totalVehicles, totalCards] = await Promise.all([
    GateCapture.count({ where: captureWhere }),
    GateCapture.count({ where: { ...captureWhere, eventType: 'access_granted' } }),
    GateCapture.count({ where: { ...captureWhere, eventType: 'access_denied' } }),
    GateCapture.count({ where: { ...captureWhere, eventType: 'security_check' } }),
    SecurityAlert.count({
      where: start || end
        ? {
            createdAt: {
              ...(start ? { [Op.gte]: start } : {}),
              ...(end ? { [Op.lte]: end } : {}),
            },
          }
        : {},
    }),
    Vehicle.count(),
    AllowedCard.count(),
  ]);

  const recentCaptures = await GateCapture.findAll({
    where: captureWhere,
    order: [['capturedAt', 'DESC']],
    limit: 10,
  });

  const recentAlerts = await SecurityAlert.findAll({
    where: start || end
      ? {
          createdAt: {
            ...(start ? { [Op.gte]: start } : {}),
            ...(end ? { [Op.lte]: end } : {}),
          },
        }
      : {},
    order: [['createdAt', 'DESC']],
    limit: 10,
  });

  return {
    dateRange: {
      startDate: start ? start.toISOString() : null,
      endDate: end ? end.toISOString() : null,
    },
    totals: {
      totalCaptures,
      accessGranted,
      accessDenied,
      securityChecks,
      totalAlerts,
      totalVehicles,
      totalCards,
    },
    recentCaptures,
    recentAlerts,
  };
};

const getSummary = async (req, res, next) => {
  try {
    const summary = await fetchSummaryData(req);
    return res.json(summary);
  } catch (error) {
    return next(error);
  }
};

const exportPdf = async (req, res, next) => {
  try {
    const summary = await fetchSummaryData(req);
    const doc = new PDFDocument({ margin: 40, size: 'A4' });

    const fileName = `toll-gate-report-${new Date().toISOString().slice(0, 10)}.pdf`;
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="${fileName}"`);

    doc.pipe(res);

    doc.fontSize(20).text('Intelligent Toll Gate System Report', { align: 'center' });
    doc.moveDown(0.5);
    doc.fontSize(10).fillColor('#555').text(`Generated by: ${req.user.fullName} (${req.user.role})`, { align: 'center' });
    doc.text(`Generated at: ${new Date().toLocaleString()}`, { align: 'center' });
    doc.moveDown();

    if (summary.dateRange.startDate || summary.dateRange.endDate) {
      doc.fontSize(11).fillColor('#000').text('Date Range', { underline: true });
      doc.fontSize(10).text(`Start: ${summary.dateRange.startDate || 'All time'}`);
      doc.text(`End: ${summary.dateRange.endDate || 'All time'}`);
      doc.moveDown();
    }

    doc.fontSize(11).text('System Summary', { underline: true });
    const totals = summary.totals;
    doc.fontSize(10)
      .text(`Total Captures: ${totals.totalCaptures}`)
      .text(`Access Granted: ${totals.accessGranted}`)
      .text(`Access Denied: ${totals.accessDenied}`)
      .text(`Security Checks: ${totals.securityChecks}`)
      .text(`Security Alerts: ${totals.totalAlerts}`)
      .text(`Registered Vehicles: ${totals.totalVehicles}`)
      .text(`Authorized Cards: ${totals.totalCards}`);

    doc.moveDown();
    doc.fontSize(11).text('Recent Captures', { underline: true });
    summary.recentCaptures.slice(0, 8).forEach((capture, index) => {
      doc.fontSize(9).text(
        `${index + 1}. ${new Date(capture.capturedAt).toLocaleString()} | ${capture.eventType} | Plate: ${capture.plateText || '-'} | Face: ${capture.faceName || '-'} | Decision: ${capture.securityDecision}`
      );
    });

    doc.moveDown();
    doc.fontSize(11).text('Recent Alerts', { underline: true });
    if (summary.recentAlerts.length === 0) {
      doc.fontSize(9).text('No security alerts in this period.');
    } else {
      summary.recentAlerts.slice(0, 8).forEach((alert, index) => {
        doc.fontSize(9).text(
          `${index + 1}. ${new Date(alert.createdAt).toLocaleString()} | ${alert.alertType} | ${alert.reason || '-'} `
        );
      });
    }

    doc.end();
  } catch (error) {
    return next(error);
  }
};

module.exports = {
  getSummary,
  exportPdf,
};
